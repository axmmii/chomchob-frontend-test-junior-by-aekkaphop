export function typeColorClass(type: string) {
    switch (type) {
      case 'grass':
        return 'bg-[#FFF4E3] text-[#FFAE33]';
      case 'poison':
        return 'bg-[#FFF4E3] text-[#FFAE33]';
      case 'fire':
        return 'bg-[#FFF4E3] text-[#FFAE33]';
      case 'flying':
        return 'bg-[#FFF4E3] text-[#FFAE33]';
      case 'water':
        return 'bg-[#FFF4E3] text-[#FFAE33]';
      case 'bug':
        return 'bg-[#FFF4E3] text-[#FFAE33]';
      default:
        return 'bg-[#FFF4E3] text-[#FFAE33]';
    }
  }